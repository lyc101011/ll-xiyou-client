local xys = 生成XY
local 场景类_特效点 = class()
local jls = 取两点距离
function 场景类_特效点:初始化(x,y,切换,资源)
	self.坐标 = xys(x*20,y*20)
	self.资源 = 资源
	self.切换 = 切换
-- self.特效点 =  tp.资源:载入('mapani.wdf',"网易WDF动画",0x082DB080)--荷花
end

function 场景类_特效点:显示(dt,x,y,pys)
	if jls(tp.角色坐标,self.坐标) < 全局游戏高度/2+100 then
		if self.特效点  == nil then
			self.特效点 =  引擎.场景.资源:载入(self.资源,"网易WDF动画",self.切换)
		end
	   self.特效点:显示(self.坐标 + pys)
       self.特效点:更新(dt)
    else
        if self.特效点 ~= nil then
        	self.特效点= nil
        end
    end
end

